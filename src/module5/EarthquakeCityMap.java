	/** Event handler that gets called automatically when the 
	 * mouse moves.
	 */
	@Override
	public void mouseMoved()
	{
		// clear the last selection
		if (lastSelected != null) {
			lastSelected.setSelected(false);
			lastSelected = null;
		
		}
		selectMarkerIfHover(quakeMarkers);
		selectMarkerIfHover(cityMarkers);
	}
	int t=0;
	
	// If there is a marker under the cursor, and lastSelected is null 
	// set the lastSelected to be the first marker found under the cursor
	// Make sure you do not select two markers.
	// 
	private void selectMarkerIfHover(List<Marker> markers)
	{
		// TODO: Implement this method
		if(lastSelected != null) {
		return ;
		}
		for(Marker m : markers) {
			CommonMarker cm = (CommonMarker)m;
			if(cm.isInside(map, mouseX, mouseY)) {
				lastSelected = cm;
				cm.setSelected(true);
				return;
			}
			}
		}
	
	/** The event handler for mouse clicks
	 * It will display an earthquake and its threat circle of cities
	 * Or if a city is clicked, it will display all the earthquakes 
	 * where the city is in the threat circle
	 */
	@Override
	public void mouseClicked()
	{
		// TODO: Implement this method
		// Hint: You probably want a helper method or two to keep this code
		// from getting too long/disorganized
		if(lastClicked != null) {
			unhideMarkers();
			lastClicked = null;
		}
		else {
			setSetlectedEarthquakeMarker(quakeMarkers);
			if(lastClicked == null)
			setSelectedCityMarker(cityMarkers);
			if(lastClicked == null) {
				unhideMarkers();
			}
		}
	}
	
	//made up method by me
	private void setSetlectedEarthquakeMarker(List<Marker> markers) {
		for(Marker m: markers) {
			EarthquakeMarker cm = (EarthquakeMarker) m;
			if(cm.isInside(map, mouseX, mouseY)) {
				lastClicked = cm;
				for(Marker t : cityMarkers) {
					if(t.getDistanceTo(cm.getLocation())>cm.threatCircle()){
						t.setHidden(true);
				}
			}
			}
			else {
				cm.setHidden(true);
			}
		}
	}
	private void setSelectedCityMarker(List<Marker> markers) {
		for(Marker m: markers) {
			CityMarker cm= (CityMarker) m;
			if(cm.isInside(map, mouseX, mouseY)) {
				lastClicked = cm;
				for(Marker t : quakeMarkers) {
					EarthquakeMarker ct = (EarthquakeMarker) t;
					if(cm.getDistanceTo(ct.getLocation())<=ct.threatCircle()){
						ct.setHidden(false);
				}
			}
			}
			else {
				cm.setHidden(true);
			}
		}
	}
	// loop over and unhide all markers
	private void unhideMarkers() {
		for(Marker marker : quakeMarkers) {
			marker.setHidden(false);
		}
			
		for(Marker marker : cityMarkers) {
			marker.setHidden(false);
		}
	}
	
